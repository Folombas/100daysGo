// calculateEarnings –¥–µ–º–æ–Ω—Å—Ç—Ä–∏—Ä—É–µ—Ç –ø–µ—Ä–µ–¥–∞—á—É –ø–æ –∑–Ω–∞—á–µ–Ω–∏—é:
// –∏–∑–º–µ–Ω–µ–Ω–∏—è –≤–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏ –ù–ï –≤–ª–∏—è—é—Ç –Ω–∞ –∏—Å—Ö–æ–¥–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é
func calculateEarnings(deliveries int, hourlyRate float64) (total float64) {
	// –ö–æ–ø–∏—è hourlyRate –ø–µ—Ä–µ–¥–∞—ë—Ç—Å—è –ø–æ –∑–Ω–∞—á–µ–Ω–∏—é
	hourlyRate = hourlyRate * 0.8 // –°–Ω–∏–∂–∞–µ–º —Å—Ç–∞–≤–∫—É –∏–∑-–∑–∞ –∏–∑–Ω–æ—Å–∞ –æ–±—É–≤–∏ (–ª–æ–∫–∞–ª—å–Ω–æ–µ –∏–∑–º–µ–Ω–µ–Ω–∏–µ)
	total = float64(deliveries) * hourlyRate
	return // total ‚Äî –∏–º–µ–Ω–æ–≤–∞–Ω–Ω–æ–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
}

// analyzeEnergyLevel –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç, –∫–∞–∫ –ø–µ—Ä–µ–¥–∞—á–∞ –ø–æ –∑–Ω–∞—á–µ–Ω–∏—é –∑–∞—â–∏—â–∞–µ—Ç –∏—Å—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
func analyzeEnergyLevel(energy int) (message string) {
	energy -= 20 // –õ–æ–∫–∞–ª—å–Ω–æ–µ –∏–∑–º–µ–Ω–µ–Ω–∏–µ —ç–Ω–µ—Ä–≥–∏–∏ –≤ —Ñ—É–Ω–∫—Ü–∏–∏
	if energy > 50 {
		message = "üí™ –≠–Ω–µ—Ä–≥–∏—è –≤—ã—Å–æ–∫–∞! –ú–æ–∂–Ω–æ —É—á–∏—Ç—å Go –ø–æ—Å–ª–µ —Ä–∞–±–æ—Ç—ã."
	} else if energy > 20 {
		message = "‚ö†Ô∏è –≠–Ω–µ—Ä–≥–∏—è –Ω–∞ –ø—Ä–µ–¥–µ–ª–µ. –û—Ç–¥–æ—Ö–Ω–∏ 15 –º–∏–Ω—É—Ç ‚Äî –ø–æ—Ç–æ–º –∫–æ–¥."
	} else {
		message = "üíî –≠–Ω–µ—Ä–≥–∏—è –∏—Å—á–µ—Ä–ø–∞–Ω–∞. –ù–æ –ø–æ–º–Ω–∏: –≤ IT —Ç–≤–æ—è —ç–Ω–µ—Ä–≥–∏—è –≤–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è –±—ã—Å—Ç—Ä–µ–µ."
	}
	return
}

// forecastITCareer ‚Äî –º–µ—Ç–∞—Ñ–æ—Ä–∞ –ø–µ—Ä–µ–¥–∞—á–∏ –ø–æ –∑–Ω–∞—á–µ–Ω–∏—é:
// –æ–ø—ã—Ç –∫—É—Ä—å–µ—Ä–∞ –Ω–µ –ø–µ—Ä–µ–∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç IT-–ø–æ—Ç–µ–Ω—Ü–∏–∞–ª
func forecastITCareer(weeksOfStudy int) (salary int, jobTitle string) {
	salary = 80000 + weeksOfStudy*5000
	switch {
	case weeksOfStudy < 4:
		jobTitle = "–°—Ç–∞–∂—ë—Ä"
	case weeksOfStudy < 12:
		jobTitle = "Junior Go-—Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫"
	default:
		jobTitle = "Middle Go-—Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫"
	}
	return
}

