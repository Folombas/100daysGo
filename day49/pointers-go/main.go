		fmt.Println("—Ç—ã —É–∫–∞–∑—ã–≤–∞–µ—à—å –Ω–∞ –Ω–µ—ë, –∏ —Ç–≤–æ—è —ç–Ω–µ—Ä–≥–∏—è —Ä–∞—Å—Ç—ë—Ç –æ—Ç –æ—Å–æ–∑–Ω–∞–Ω–∏—è —Ü–µ–ª–∏.")
		restoreEnergy(energy, 2.5) // –í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ —ç–Ω–µ—Ä–≥–∏–∏ –ø–æ—Å–ª–µ –æ—Ç–¥—ã—Ö–∞
		changeLocation(location, "–ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –∏–º. –õ–µ–Ω–∏–Ω–∞ (—Ç—ë–ø–ª—ã–π –∑–∞–ª, Wi-Fi, 22¬∞C)")
