// updateSalary –∏–∑–º–µ–Ω—è–µ—Ç –∑–∞—Ä–ø–ª–∞—Ç—É —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å ‚Äî –∫–∞–∫ –ø–µ—Ä–µ—Ö–æ–¥ –∏–∑ –∫—É—Ä—å–µ—Ä–∫–∏ –≤ IT
func updateSalary(currentSalary *int, careerPath string) {
	if careerPath == "IT" {
		*currentSalary = 250000 // Middle Go-—Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫
	} else {
		*currentSalary = 45000 // –°—Ä–µ–¥–Ω—è—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –∫—É—Ä—å–µ—Ä–∞
	}
}

// restoreEnergy –≤–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —ç–Ω–µ—Ä–≥–∏—é —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å ‚Äî –∫–∞–∫ –æ—Ç–¥—ã—Ö –ø–æ—Å–ª–µ –æ–∂–∏–¥–∞–Ω–∏—è —É —Ç—É—Ä–Ω–∏–∫–µ—Ç–æ–≤ –≤ –æ—Ñ–∏—Å–Ω–æ–º —Ü–µ–Ω—Ç—Ä–µ
func restoreEnergy(energy *int, restHours float64) {
	recovery := int(restHours * 20)
	if recovery > 70 {
		recovery = 70
	}
	*energy += recovery
	if *energy > 100 {
		*energy = 100
	}
}

// changeLocation –º–µ–Ω—è–µ—Ç –º–µ—Å—Ç–æ–ø–æ–ª–æ–∂–µ–Ω–∏–µ —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å ‚Äî –∏–∑ —Ö–æ–ª–æ–¥–∞ –≤ –æ—Ñ–∏—Å
func changeLocation(location *string, newPlace string) {
	*location = newPlace
}

// simulateWait –∏–º–∏—Ç–∏—Ä—É–µ—Ç –æ–∂–∏–¥–∞–Ω–∏–µ —É —Ç—É—Ä–Ω–∏–∫–µ—Ç–æ–≤ —Å –ø–æ—Ç–µ—Ä–µ–π —ç–Ω–µ—Ä–≥–∏–∏
func simulateWait(energy *int, minutes int) {
	loss := minutes * 2
	if *energy-loss < 0 {
		*energy = 0
	} else {
		*energy -= loss
	}
}

