    fmt.Println(localVar)
    fmt.Println(globalVar) // –ì–ª–æ–±–∞–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–æ—Å—Ç—É–ø–Ω—ã

    // –ó–∞—Ç–µ–Ω–µ–Ω–∏–µ –≥–ª–æ–±–∞–ª—å–Ω–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π
    globalVar := "–Ø –∑–∞—Ç–µ–Ω—è—é –≥–ª–æ–±–∞–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –≤–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏"
    fmt.Println("–ó–∞—Ç–µ–Ω–µ–Ω–Ω–∞—è:", globalVar)

    // –ë–ª–æ—á–Ω–∞—è –æ–±–ª–∞—Å—Ç—å –≤–∏–¥–∏–º–æ—Å—Ç–∏
    if true {
        blockVar := "–Ø –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –≤–Ω—É—Ç—Ä–∏ –±–ª–æ–∫–∞ if"
        fmt.Println(blockVar)
        fmt.Println(localVar) // –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –≤–Ω–µ—à–Ω–µ–π —Ñ—É–Ω–∫—Ü–∏–∏ –¥–æ—Å—Ç—É–ø–Ω—ã

        // –ó–∞—Ç–µ–Ω–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π —Ñ—É–Ω–∫—Ü–∏–∏
        localVar := "–Ø –∑–∞—Ç–µ–Ω—è—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é —Ñ—É–Ω–∫—Ü–∏–∏ –≤–Ω—É—Ç—Ä–∏ –±–ª–æ–∫–∞"
        fmt.Println("–ó–∞—Ç–µ–Ω–µ–Ω–Ω–∞—è localVar:", localVar)
    }

    // blockVar –∑–¥–µ—Å—å —É–∂–µ –Ω–µ –¥–æ—Å—Ç—É–ø–Ω–∞!
    // fmt.Println(blockVar) - —ç—Ç–æ –≤—ã–∑–≤–∞–ª–æ –±—ã –æ—à–∏–±–∫—É –∫–æ–º–ø–∏–ª—è—Ü–∏–∏
