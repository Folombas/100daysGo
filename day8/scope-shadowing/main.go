    // –î–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è –∑–∞—Ç–µ–Ω–µ–Ω–∏—è –≤ —Ü–∏–∫–ª–∞—Ö
    fmt.Println("\n=== –ó–∞—Ç–µ–Ω–µ–Ω–∏–µ –≤ —Ü–∏–∫–ª–∞—Ö ===")
    counter := 10
    fmt.Printf("counter –¥–æ —Ü–∏–∫–ª–∞: %d\n", counter)

