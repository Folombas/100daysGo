    fmt.Println("   –º–Ω–æ–∂–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫ –∏ –¥–µ–ª–∞–µ—Ç –∫–æ–¥ –ø—Ä–µ–¥—Å–∫–∞–∑—É–µ–º—ã–º!")
