package main

import (
	"fmt"
	"time"
)

func main() {
	// –ö–æ–Ω–≤–µ–π–µ—Ä (Pipeline) –¥–Ω–µ–π —Å–æ–ø—Ä–æ—Ç–∏–≤–ª–µ–Ω–∏—è
	days := generateDaysSince() // –≠—Ç–∞–ø 1: –ì–µ–Ω–µ—Ä–∞—Ü–∏—è
	resistanceDays := filterAndCount(days) // –≠—Ç–∞–ø 2: –§–∏–ª—å—Ç—Ä–∞—Ü–∏—è –∏ –ø–æ–¥—Å—á—ë—Ç
	result := formatMessage(resistanceDays) // –≠—Ç–∞–ø 3: –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ

	// –≠—Ç–∞–ø 4: –í—ã–≤–æ–¥ (—Ñ–∏–Ω–∞–ª—å–Ω—ã–π —ç—Ç–∞–ø –∫–æ–Ω–≤–µ–π–µ—Ä–∞)
	fmt.Println(result)
	printMotivationalPhrase(resistanceDays)
}

// –≠—Ç–∞–ø 1 –∫–æ–Ω–≤–µ–π–µ—Ä–∞: –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä. –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –≤ –∫–∞–Ω–∞–ª –≤—Å–µ –¥–Ω–∏ —Å –¥–∞—Ç—ã —É–¥–∞–ª–µ–Ω–∏—è.
func generateDaysSince() <-chan time.Time {
	out := make(chan time.Time)
	startDate := time.Date(2026, time.January, 18, 0, 0, 0, 0, time.UTC)
	go func() {
		for d := startDate; ; d = d.AddDate(0, 0, 1) {
			out <- d
			if d.After(time.Now().UTC()) {
				close(out)
				break
			}
		}
	}()
	return out
}

// –≠—Ç–∞–ø 2 –∫–æ–Ω–≤–µ–π–µ—Ä–∞: –§–∏–ª—å—Ç—Ä –∏ —Å—á—ë—Ç—á–∏–∫. –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ—Ç –¥–Ω–∏ –¥–æ —Ç–µ–∫—É—â–µ–π –¥–∞—Ç—ã.
func filterAndCount(days <-chan time.Time) int {
	count := 0
	for day := range days {
		if day.Before(time.Now().UTC()) || day.Equal(time.Now().Truncate(24*time.Hour).UTC()) {
			count++
		}
	}
	return count
}

