func demoConsoleWidthFormatting() {
	fmt.Println("=== üñ•Ô∏è  –î–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —à–∏—Ä–∏–Ω—ã –≤ –∫–æ–Ω—Å–æ–ª–∏ ===\n")

	// –ü—Ä–∏–º–µ—Ä 1: –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —à–∏—Ä–∏–Ω–∞ ‚Äî –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ –ø–æ –ø—Ä–∞–≤–æ–º—É –∫—Ä–∞—é
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 1: –í—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ –ø–æ –ø—Ä–∞–≤–æ–º—É –∫—Ä–∞—é (—à–∏—Ä–∏–Ω–∞ 10)")
	fmt.Printf("|%10s|\n", "Go")
	fmt.Printf("|%10s|\n", "Golang")
	fmt.Printf("|%10s|\n", "–ü—Ä–∏–≤–µ—Ç")
	fmt.Println()

	// –ü—Ä–∏–º–µ—Ä 2: –í—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ –ø–æ –ª–µ–≤–æ–º—É –∫—Ä–∞—é
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 2: –í—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ –ø–æ –ª–µ–≤–æ–º—É –∫—Ä–∞—é (—à–∏—Ä–∏–Ω–∞ 10)")
	fmt.Printf("|%-10s|\n", "Go")
	fmt.Printf("|%-10s|\n", "Golang")
	fmt.Printf("|%-10s|\n", "–ü—Ä–∏–≤–µ—Ç")
	fmt.Println()

	// –ü—Ä–∏–º–µ—Ä 3: –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –Ω—É–ª—è–º–∏
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 3: –ó–∞–ø–æ–ª–Ω–µ–Ω–∏–µ —á–∏—Å–ª–æ–≤–æ–π —à–∏—Ä–∏–Ω—ã –Ω—É–ª—è–º–∏")
	fmt.Printf("ID: %06d\n", 42)
	fmt.Printf("–ö–æ–¥: %08d\n", 123)
	fmt.Printf("–°—á—ë—Ç: %05d\n", 7)
	fmt.Println()

	// –ü—Ä–∏–º–µ—Ä 4: –î–∏–Ω–∞–º–∏—á–µ—Å–∫–∞—è —à–∏—Ä–∏–Ω–∞ —á–µ—Ä–µ–∑ *
	width := 15
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 4: –î–∏–Ω–∞–º–∏—á–µ—Å–∫–∞—è —à–∏—Ä–∏–Ω–∞ (—á–µ—Ä–µ–∑ *)")
	fmt.Printf("|%*s|\n", width, "–î–∏–Ω–∞–º–∏—á–µ—Å–∫–∏")
	fmt.Printf("|%*s|\n", width, "Go")
	fmt.Printf("|%*s|\n", width, "–§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ")
	fmt.Println()

	// –ü—Ä–∏–º–µ—Ä 5: –®–∏—Ä–∏–Ω–∞ –¥–ª—è —á–∏—Å–µ–ª —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 5: –®–∏—Ä–∏–Ω–∞ + —Ç–æ—á–Ω–æ—Å—Ç—å –¥–ª—è float")
	fmt.Printf("|%10.2f|\n", 3.14)
	fmt.Printf("|%10.2f|\n", 123.456)
	fmt.Printf("|%10.2f|\n", 9999.999)
	fmt.Println()

	// –ü—Ä–∏–º–µ—Ä 6: –ö–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —à–∏—Ä–∏–Ω—ã, –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏—è –∏ –∫–∏—Ä–∏–ª–ª–∏—Ü—ã
	fmt.Println("üîπ –ü—Ä–∏–º–µ—Ä 6: –¢–∞–±–ª–∏—Ü–∞ —Å –∫–∏—Ä–∏–ª–ª–∏—Ü–µ–π –∏ –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ–º")
	headers := []string{"–ò–º—è", "–í–æ–∑—Ä–∞—Å—Ç", "–ì–æ—Ä–æ–¥"}
	data := [][]string{
		{"–ê–ª–µ–∫—Å–µ–π", "28", "–ú–æ—Å–∫–≤–∞"},
		{"–ú–∞—Ä–∏—è", "32", "–°–∞–Ω–∫—Ç-–ü–µ—Ç–µ—Ä–±—É—Ä–≥"},
		{"–î–º–∏—Ç—Ä–∏–π", "25", "–ù–æ–≤–æ—Å–∏–±–∏—Ä—Å–∫"},
	}

	// –ü–µ—á–∞—Ç—å –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
	fmt.Printf("| %-10s | %6s | %-15s |\n", headers[0], headers[1], headers[2])
	fmt.Println("|------------|--------|-----------------|")

	// –ü–µ—á–∞—Ç—å –¥–∞–Ω–Ω—ã—Ö
	for _, row := range data {
		fmt.Printf("| %-10s | %6s | %-15s |\n", row[0], row[1], row[2])
	}
	fmt.Println()

	fmt.Println("‚úÖ –ö–æ–Ω—Å–æ–ª—å–Ω–∞—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∞!")
}
