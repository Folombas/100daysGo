func daysSince(dateStr string) int {
	t, _ := time.Parse(time.DateOnly, dateStr)
	t = t.UTC()
	now := time.Now().UTC()
	days := int(now.Sub(t).Hours() / 24)
	return clamp(days, 0, challengeDays)
}

