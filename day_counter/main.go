// üìä –†–µ–∞–ª—å–Ω—ã–π –ø–æ–¥—Å—á—ë—Ç —Å—Ç—Ä–æ–∫ –∫–æ–¥–∞ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–Ω—è
func countGoLines(dirPath string) (int, error) {
	totalLines := 0
	err := filepath.Walk(dirPath, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		// –°—á–∏—Ç–∞–µ–º –¢–û–õ–¨–ö–û .go —Ñ–∞–π–ª—ã
		if !info.IsDir() && strings.HasSuffix(path, ".go") {
			file, err := os.Open(path)
			if err != nil {
				return err
			}
			defer file.Close()

			scanner := bufio.NewScanner(file)
			lineCount := 0
			for scanner.Scan() {
				// –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏ –∏ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ)
				line := strings.TrimSpace(scanner.Text())
				if line != "" && !strings.HasPrefix(line, "//") {
					lineCount++
				}
			}
			if err := scanner.Err(); err != nil {
				return err
			}
			totalLines += lineCount
		}
		return nil
	})
	return totalLines, err
}

