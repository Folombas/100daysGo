// –ê–Ω–∞–ª–∏–∑ –¥–Ω—è: —É—Ç—Ä–æ, –¥–µ–Ω—å –∏–ª–∏ –≤–µ—á–µ—Ä
func getPartOfDay() string {
	hour := time.Now().Hour()

	switch {
	case hour >= 5 && hour < 12:
		return "–£—Ç—Ä–æ"
	case hour >= 12 && hour < 18:
		return "–î–µ–Ω—å"
	case hour >= 18 && hour < 23:
		return "–í–µ—á–µ—Ä"
	default:
		return "–ù–æ—á—å"
	}
}

