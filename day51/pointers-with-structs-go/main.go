func interactiveSecondHalf(profile *CourierProfile) {
	fmt.Println("\n" + strings.Repeat("=", 60))
	fmt.Printf("%süî• –ü–õ–ê–ù–´ –ù–ê –í–¢–û–†–£–Æ –ü–û–õ–û–í–ò–ù–£ (DAY 52-100):%s\n", ansi("1;31"), ansi("0"))
	fmt.Println("1. –£—Ç—Ä–æ: 2 —á–∞—Å–∞ –∫–æ–¥–∞ ‚Üí –î–µ–Ω—å: 3 –∑–∞–∫–∞–∑–∞ + –≤–µ—á–µ—Ä–æ–º –æ–±—É—á–µ–Ω–∏–µ")
	fmt.Println("2. –ü–æ–ª–Ω—ã–π —Ñ–æ–∫—É—Å –Ω–∞ Go: 4 —á–∞—Å–∞ –∫–æ–¥–∞ –≤ –±–∏–±–ª–∏–æ—Ç–µ–∫–µ")
	fmt.Println("3. –ë–∞–ª–∞–Ω—Å: 1 –∑–∞–∫–∞–∑ —É—Ç—Ä–æ–º + 3 —á–∞—Å–∞ –∫–æ–¥–∞ + —Å–ø–æ—Ä—Ç–∑–∞–ª –≤–µ—á–µ—Ä–æ–º")

	reader := bufio.NewReader(os.Stdin)
	var choice string
	var err error

	// –í–∞–ª–∏–¥–∞—Ü–∏—è –≤–≤–æ–¥–∞
	for {
		fmt.Print("\n–í–∞—à –≤—ã–±–æ—Ä (1-3): ")
		choice, err = reader.ReadString('\n')
		if err != nil {
			fmt.Printf("%s‚ùå –û—à–∏–±–∫–∞ –≤–≤–æ–¥–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑.%s\n", ansi("1;31"), ansi("0"))
			continue
		}
		choice = strings.TrimSpace(choice)
		if choice == "1" || choice == "2" || choice == "3" {
			break
		}
		fmt.Printf("%s‚ö†Ô∏è –¢–æ–ª—å–∫–æ 1, 2 –∏–ª–∏ 3!%s\n", ansi("1;33"), ansi("0"))
	}

	fmt.Println("\n" + strings.Repeat("-", 30))

	// –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤—ã–±–æ—Ä–∞ —Å –∏–∑–º–µ–Ω–µ–Ω–∏–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—ã —á–µ—Ä–µ–∑ —É–∫–∞–∑–∞—Ç–µ–ª—å
	switch choice {
	case "1":
		fmt.Printf("%s‚öñÔ∏è –°–¢–†–ê–¢–ï–ì–ò–Ø –í–´–ñ–ò–í–ê–ù–ò–Ø:%s\n", ansi("1;33"), ansi("0"))
		fmt.Println("–•–æ—Ä–æ—à–æ –¥–ª—è —Å—Ç–∞—Ä—Ç–∞, –Ω–æ –ø–æ–º–Ω–∏: —É–∫–∞–∑–∞—Ç–µ–ª–∏ —Ç—Ä–µ–±—É—é—Ç —è–≤–Ω–æ–≥–æ –∞–¥—Ä–µ—Å–∞ —Ü–µ–ª–∏.")
		fmt.Println("–¢–≤–æ–π —É–∫–∞–∑–∞—Ç–µ–ª—å –¥–æ–ª–∂–µ–Ω –≤–µ—Å—Ç–∏ –∫ '–æ—Ñ–∏—Å—É', –∞ –Ω–µ –∫ '—ç–ª–µ–∫—Ç—Ä–∏—á–∫–∞–º'.")
		profile.Salary += 10000 // –ú–∞–ª–µ–Ω—å–∫–∏–π —à–∞–≥
	case "2":
		fmt.Printf("%s –ê–ô–¢–ò–®–ù–ê–Ø –¢–†–ê–ù–°–§–û–†–ú–ê–¶–ò–Ø:%s\n", ansi("1;32"), ansi("0"))
		fmt.Println("–ò–¥–µ–∞–ª—å–Ω–æ –¥–ª—è –≤—Ç–æ—Ä–æ–π –ø–æ–ª–æ–≤–∏–Ω—ã! –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ = —Ç–≤–æ–π –Ω–æ–≤—ã–π –æ—Ñ–∏—Å-–ø—Ä–æ—Ç–æ—Ç–∏–ø.")
		fmt.Println("–ß–µ—Ä–µ–∑ 50 –¥–Ω–µ–π —ç—Ç–æ—Ç –≤—ã–±–æ—Ä –ø—Ä–µ–≤—Ä–∞—Ç–∏—Ç —Ç–µ–±—è –≤ 'Middle Go-—Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–∞'.")
		profile.Salary += 50000 // –ë—ã—Å—Ç—Ä—ã–π —Ä–æ—Å—Ç
		profile.Energy = 85
		profile.Location = "–ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –∏–º. –õ–µ–Ω–∏–Ω–∞ (Wi-Fi, —Ç–∏—à–∏–Ω–∞, 22¬∞C)"
	case "3":
		fmt.Printf("%süßò –ì–ê–†–ú–û–ù–ò–ß–ù–´–ô –ü–£–¢–¨:%s\n", ansi("1;34"), ansi("0"))
		fmt.Println("–°–ø–æ—Ä—Ç–∑–∞–ª –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç —Ç–µ–ª–æ –ø–æ—Å–ª–µ –∫—É—Ä—å–µ—Ä—Å–∫–∏—Ö –ª–µ—Ç. –ö–æ–¥ ‚Äî —Ä–∞–∑—É–º.")
		fmt.Println("–ù–æ –ø–æ–º–Ω–∏: —É–∫–∞–∑–∞—Ç–µ–ª–∏ —Ä–∞–±–æ—Ç–∞—é—Ç —Ç–æ–ª—å–∫–æ –ø—Ä–∏ —á—ë—Ç–∫–æ–π —Ü–µ–ª–∏. –ù–µ —Ä–∞—Å–ø—ã–ª—è–π—Å—è.")
		profile.Energy = 90
	}

	// –û–±–Ω–æ–≤–ª—ë–Ω–Ω—ã–π –ø—Ä–æ–≥–Ω–æ–∑
	fmt.Printf("\n%süéØ –ü–†–û–ì–ù–û–ó –ö DAY 100:%s\n", ansi("1;35"), ansi("0"))
	fmt.Printf("   üíº –î–æ–ª–∂–Ω–æ—Å—Ç—å: %s\n", profile.CurrentJob)
	fmt.Printf("   üí∞ –ó–∞—Ä–ø–ª–∞—Ç–∞: %d ‚ÇΩ/–º–µ—Å\n", profile.Salary)
	fmt.Printf("   üåç –ú–µ—Å—Ç–æ: %s\n", profile.Location)
	fmt.Printf("   ‚úÖ –°—Ç–∞—Ç—É—Å: %s\n",
		func() string {
			if profile.Salary >= 200000 {
				return "–ì–û–¢–û–í –ö –°–û–ë–ï–°–ï–î–û–í–ê–ù–ò–Æ"
			}
			return "–ù–ê –ü–£–¢–ò –ö –¶–ï–õ–ò"
		}())
}

