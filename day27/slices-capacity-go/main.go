// üì¶ –î–û–ë–ê–í–õ–ï–ù–ò–ï –ó–ê–ö–ê–ó–ê –° –ê–ù–ê–õ–ò–ó–û–ú CAPACITY
func appendOrder(orders []string, order string) []string {
	oldLen, oldCap := len(orders), cap(orders)
	orders = append(orders, order)
	newLen, newCap := len(orders), cap(orders)

	fmt.Printf("   ‚úÖ –ó–∞–∫–∞–∑: \"%s\"\n", order)
	fmt.Printf("      ‚Ä¢ –ë—ã–ª–æ: len=%d, cap=%d\n", oldLen, oldCap)
	fmt.Printf("      ‚Ä¢ –°—Ç–∞–ª–æ: len=%d, cap=%d\n", newLen, newCap)

	// –ê–Ω–∞–ª–∏–∑ —Ä–æ—Å—Ç–∞ capacity
	if newCap > oldCap {
		fmt.Printf("      ‚Ä¢ üí• –†–ï–ê–õ–õ–û–ö–ê–¶–ò–Ø! Capacity –≤—ã—Ä–æ—Å–ª–∞ –Ω–∞ %d%%\n",
			(newCap-oldCap)*100/oldCap)
		if strings.Contains(order, "—Å–µ–±—è") {
			fmt.Println("      ‚Ä¢ üíî –ú–Ω–µ –ø—Ä–∏—à–ª–æ—Å—å –æ—Ç–¥–∞—Ç—å —Å–≤–æ–∏ 38 —Ä–æ–∑ –∫–ª–∏–µ–Ω—Ç—É... –ù–æ –º–æ—è –º–µ—á—Ç–∞ –æ—Å—Ç–∞–Ω–µ—Ç—Å—è —Å–æ –º–Ω–æ–π")
		}
	}

	return orders
}

