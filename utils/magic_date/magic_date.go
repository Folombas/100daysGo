// –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å-–±–∞—Ä–∞
func generateProgressBar(current, total int) string {
	width := 30
	percent := float64(current) / float64(total)
	filled := int(float64(width) * percent)
	empty := width - filled

	bar := "„Äê"
	for i := 0; i < filled; i++ {
		bar += "‚ñ†"
	}
	for i := 0; i < empty; i++ {
		bar += "‚îÄ"
	}
	bar += "„Äë"

	return bar
}
