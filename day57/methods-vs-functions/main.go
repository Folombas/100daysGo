// –°–¢–ê–ù–î–ê–†–¢–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø: –ö—É–ø–∏—Ç—å –≥–∞–¥–∂–µ—Ç—ã (–∫–∞–∫ —Ç–æ—Ç –ø–æ–∫—É–ø–∞—Ç–µ–ª—å –≤ –¢–¶)
func BuyGadgets(cash float64) string {
	gadgets := []string{"iPhone Pro", "MacBook Pro", "AirPods Max", "iPad Pro", "Apple Watch Ultra"}
	totalCost := 0.0
	bought := []string{}

	// –ü–æ–∫—É–ø–∞–µ–º, –ø–æ–∫–∞ –µ—Å—Ç—å –¥–µ–Ω—å–≥–∏ (–∫–∞–∫ —Ç–æ—Ç –ø–æ–º—è—Ç—ã–π –Ω–æ—Å–æ–∫ —Å –º–∏–ª–ª–∏–æ–Ω–æ–º)
	for _, gadget := range gadgets {
		price := 150000.0 // –°—Ä–µ–¥–Ω—è—è —Ü–µ–Ω–∞ –≥–∞–¥–∂–µ—Ç–∞ –≤ –ú–æ—Å–∫–≤–µ
		if cash >= price {
			cash -= price
			totalCost += price
			bought = append(bought, gadget)
		}
	}

	summary := fmt.Sprintf("üõçÔ∏è –ü–æ–∫—É–ø–∞—Ç–µ–ª—å –∫—É–ø–∏–ª:\n   - %s\n   üíµ –ò—Ç–æ–≥–æ: %.0f ‚ÇΩ (–æ—Å—Ç–∞–ª–æ—Å—å: %.0f ‚ÇΩ)",
		strings.Join(bought, "\n   - "),
		totalCost,
		cash,
	)

	// –ê–Ω–∞–ª–æ–≥–∏—è –∏–∑ –ª–µ–≥–µ–Ω–¥—ã
	if len(bought) > 0 {
		summary += "\n   üëî –í–Ω–µ—à–Ω–æ—Å—Ç—å: –ø–æ–º—è—Ç—ã–π –Ω–æ—Å–æ–∫. –°—É—Ç—å: —É—Å–ø–µ—à–Ω—ã–π —á–µ–ª–æ–≤–µ–∫."
	} else {
		summary += "\n   üò¢ –ù–µ—Ç –¥–µ–Ω–µ–≥ ‚Äî –Ω–µ—Ç –≥–∞–¥–∂–µ—Ç–æ–≤. –ù–æ –µ—Å—Ç—å –∫–æ–¥!"
	}

	return summary
}

