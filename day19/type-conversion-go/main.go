	fmt.Println("üé§ –ì–û–®–ê –¢–ò–•–û –†–ê–ü–£–ï–¢ –ü–û–î –®–£–ú –ö–û–õ–Å–° (CONVERTING LIFE SCENARIOS):")
	fmt.Println(rapperEra)
	fmt.Println(currentLife)
	fmt.Println(dreamJob)
	fmt.Println("\nüì¶ –î–û–°–¢–ê–í–ö–ê ‚Ññ19: –ö–û–ù–í–ï–†–¢–ê–¶–ò–Ø –ü–†–û–ì–†–ê–ú–ú–ò–°–¢–ê...")

	// –í—ã–≤–æ–¥–∏–º –¥–∞–Ω–Ω—ã–µ —Å –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏–µ–π —Ç–∏–ø–æ–≤
	fmt.Printf("üìã –ó–∞–∫–∞–∑–∞–Ω–æ: %s –∫–≥ (string) ‚Üí —Å–∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–æ –≤ int: %d –∫–≥\n",
		declaredWeightStr, declaredWeight)
	fmt.Printf("‚öñÔ∏è  –§–ê–ö–¢: %.1f –∫–≥ (float64) ‚Üí –ø–µ—Ä–µ–≥—Ä—É–∑–∫–∞ –Ω–∞ %.1f –∫–≥!\n",
		actualWeight, weightDiscrepancy)
	fmt.Printf("üí∞ –û–±–µ—â–∞–Ω–æ —á–∞–µ–≤—ã—Ö: %d ‚ÇΩ (int) ‚Üí –ø–æ–ª—É—á–µ–Ω–æ: %d ‚ÇΩ (float64 –Ω–µ —Å–ø–∞—Å—ë—Ç –æ—Ç –∂–∞–¥–Ω–æ—Å—Ç–∏)\n",
		tip, tipReality)
	fmt.Printf("üöó –ö–ª–∏–µ–Ω—Ç –Ω–∞ %s ‚Üí –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞–ª –º–æ–∏ —Å–∏–ª—ã –≤ –ù–ò–ß–¢–û\n", clientCar)

	// –ú–æ—Ç–∏–≤–∞—Ü–∏–æ–Ω–Ω—ã–π —Ä—ç–ø-–±—Ä–∏–¥–∂ –ì–æ—à–∏ –æ type conversion
	time.Sleep(2 * time.Second)
	fmt.Println("\nüé§ [–ì–û–õ–û–° –ì–û–®–ò –í–ù–£–¢–†–ò –ì–û–õ–û–í–´]:")
	fmt.Println("\"–†–∞–Ω—å—à–µ —è –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä–æ–≤–∞–ª —Ç—Ä–µ–∫–∏ –≤ —Ö–∏—Ç—ã,")
