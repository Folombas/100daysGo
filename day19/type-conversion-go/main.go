	fmt.Println("–ü—É—Å—Ç—å –∑–∞–∫–∞–∑—ã –≤—Ä—É—Ç –ø—Ä–æ –≤–µ—Å –∏ —á–∞–µ–≤—ã–µ ‚Äî")
	fmt.Println("–Ø –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É—é –±–æ–ª—å –≤ –∫–æ–¥, –º—ã –Ω–µ —Å–≤—è—Ç—ã–µ!\"")

