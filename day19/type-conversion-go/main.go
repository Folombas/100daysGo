	// –î–∞–Ω–Ω—ã–µ –∏–∑ –∫—É—Ä—å–µ—Ä—Å–∫–æ–≥–æ –∑–∞–∫–∞–∑–∞ (–ø–æ—Å—Ç—É–ø–∞—é—Ç –∫–∞–∫ —Å—Ç—Ä–æ–∫–∏ –∏–∑ API)
