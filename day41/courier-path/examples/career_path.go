// –§—É–Ω–∫—Ü–∏—è –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞ –ø–æ—Ç–µ–Ω—Ü–∏–∞–ª—å–Ω–æ–≥–æ –¥–æ—Ö–æ–¥–∞ –ø—Ä–∏ —Å–º–µ–Ω–µ –ø—Ä–æ—Ñ–µ—Å—Å–∏–∏
func CalculateIncomeGrowth(yearsExperience int) (int, int) {
	courierSalary := 65000 + yearsExperience*1500
	developerSalary := 0

	if yearsExperience == 0 {
		developerSalary = 85000 // Intern
	} else if yearsExperience <= 2 {
		developerSalary = 150000 // Junior
	} else if yearsExperience <= 4 {
		developerSalary = 250000 // Middle
	} else if yearsExperience <= 6 {
		developerSalary = 375000 // Senior
	} else {
		developerSalary = 500000 // Team Lead
	}

	growth := developerSalary - courierSalary
	percentage := (growth * 100) / courierSalary

	return percentage, developerSalary
}

