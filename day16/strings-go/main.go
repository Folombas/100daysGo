	fmt.Println("\nüí° –ù–ï–ô–†–û-–ü–†–ê–í–î–ê –î–ù–Ø: \"–ö–∞–∂–¥–∞—è —Å—Ç—Ä–æ–∫–∞ –≤ Go ‚Äî —à–∞–≥ –æ—Ç —Å—É–≥—Ä–æ–±–∞ –∫ —Ç–µ–ø–ª—É. –ù–µ –±—Ä–æ—Å–∞–π —Å–≤–æ–π Day16!\"")
