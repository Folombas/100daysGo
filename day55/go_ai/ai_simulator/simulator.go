package ai_simulator

import (
	"strings"
)

// GenerateText симулирует "генерацию ИИ" на основе шаблонов — без ML, только Go!
// Это демонстрация того, как можно работать с "ИИ-подобными" задачами без GPU.
func GenerateText(prompt string) string {
	lowerPrompt := strings.ToLower(prompt)

	var response string

	if strings.Contains(lowerPrompt, "go") && strings.Contains(lowerPrompt, "видеокарт") {
		response = `Это — история о человеке, который не ждёт идеальных условий.
Он учит Go ночами, генерирует идеи в ComfyUI днём, и даже если его GTX 1060 зависает — он не сдаётся.
Потому что он знает: каждый написанный им файл — это шаг к новому компьютеру.
К новым моделям. К новой жизни.
Он не просто кодит — он строит своё будущее. И это — самая мощная нейросеть из всех.`
	} else {
		response = `Go — это не просто язык. Это инструмент, который работает там, где другие сдаются.
Ты можешь запускать серверы, обрабатывать запросы, управлять моделями — даже если у тебя 16 ГБ ОЗУ и GTX 1060.
Потому что Go — это эффективность. Это скорость. Это твой путь в мир ИИ.`
	}

	return response
}
