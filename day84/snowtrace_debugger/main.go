// CustomError ‚Äî –Ω–∞—à–∞ –∫–∞—Å—Ç–æ–º–Ω–∞—è –æ—à–∏–±–∫–∞ —Å –∫–æ–Ω—Ç–µ–∫—Å—Ç–æ–º –∏ —Å—Ç–µ–∫—Ç—Ä–µ–π—Å–æ–º
type CustomError struct {
	Message    string
	Context    string
	StackTrace string
	Timestamp  time.Time
}

func (e CustomError) Error() string {
	return fmt.Sprintf("‚ùå [%s] %s\n   –ö–æ–Ω—Ç–µ–∫—Å—Ç: %s\n   –¢—Ä–∞—Å—Å–∏—Ä–æ–≤–∫–∞:\n%s",
		e.Timestamp.Format("15:04:05"),
		e.Message,
		e.Context,
		e.StackTrace)
}

// getStackTrace –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ç–µ–∫—É—â–∏–π —Å—Ç–µ–∫ –≤—ã–∑–æ–≤–æ–≤
func getStackTrace() string {
	buf := make([]byte, 1024)
	n := runtime.Stack(buf, false)
	return string(buf[:n])
}

