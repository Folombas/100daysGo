// Deliver –ø—ã—Ç–∞–µ—Ç—Å—è –¥–æ—Å—Ç–∞–≤–∏—Ç—å –∑–∞–∫–∞–∑ —Å –æ–±—Ä–∞–±–æ—Ç–∫–æ–π –æ—à–∏–±–æ–∫
func (c *Courier) Deliver(address string, hasSnowdrift bool) error {
	if !c.IsDebugMode {
		fmt.Println("‚ö†Ô∏è  –†–µ–∂–∏–º –æ—Ç–ª–∞–¥–∫–∏ –≤—ã–∫–ª—é—á–µ–Ω. –û—à–∏–±–∫–∏ –±—É–¥—É—Ç —Å–∫—Ä—ã—Ç—ã.")
	}

	// –°–∏–º—É–ª—è—Ü–∏—è –æ—à–∏–±–∫–∏ "—Å–Ω–µ–∂–Ω—ã–π —Å—É–≥—Ä–æ–±"
	if hasSnowdrift {
		err := CustomError{
			Message:    "–Ω–µ–≤–æ–∑–º–æ–∂–Ω–æ –ø—Ä–æ–π—Ç–∏ –∫ –∞–¥—Ä–µ—Å—É",
			Context:    fmt.Sprintf("–∞–¥—Ä–µ—Å: %s, –∫—É—Ä—å–µ—Ä: %s", address, c.Name),
			StackTrace: getStackTrace(),
			Timestamp:  time.Now(),
		}

		if c.IsDebugMode {
			// –î–µ—Ç–∞–ª—å–Ω—ã–π –≤—ã–≤–æ–¥ —Å–æ —Å—Ç–µ–∫—Ç—Ä–µ–π—Å–æ–º
			log.Printf("üö® –î–ï–¢–ê–õ–¨–ù–ê–Ø –û–¢–õ–ê–î–ö–ê:\n%s\n", err)
			c.Understanding += 15
			fmt.Printf("   üìà –ü–æ–Ω–∏–º–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º—ã +15 (—Ç–µ–ø–µ—Ä—å: %d)\n", c.Understanding)
		} else {
			// –ü—Ä–æ—Å—Ç–æ–π –≤—ã–≤–æ–¥ –±–µ–∑ –¥–µ—Ç–∞–ª–µ–π
			fmt.Printf("‚ùå –û—à–∏–±–∫–∞: –Ω–µ –º–æ–≥—É –¥–æ–π—Ç–∏ –¥–æ %s\n", address)
			c.Willpower -= 10
		}

		// –û–±–æ—Ä–∞—á–∏–≤–∞–µ–º –æ—à–∏–±–∫—É —Å –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–º –∫–æ–Ω—Ç–µ–∫—Å—Ç–æ–º
		wrappedErr := fmt.Errorf("–¥–æ—Å—Ç–∞–≤–∫–∞ –ø—Ä–æ–≤–∞–ª–µ–Ω–∞: %w (—Å—É–≥—Ä–æ–±)", err)
		return wrappedErr
	}

	// –£—Å–ø–µ—à–Ω–∞—è –¥–æ—Å—Ç–∞–≤–∫–∞
	fmt.Printf("‚úÖ –î–æ—Å—Ç–∞–≤–ª–µ–Ω–æ: %s\n", address)
	c.Willpower += 5
	return nil
}

