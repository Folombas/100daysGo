// HandlePanic –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –ø–∞–Ω–∏–∫—É (–¥–µ–ø—Ä–µ—Å—Å–∏—é) —Å –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ–º
func (c *Courier) HandlePanic() {
	defer func() {
		if r := recover(); r != nil {
			fmt.Printf("\nüöë –í–û–°–°–¢–ê–ù–û–í–õ–ï–ù–ò–ï –ü–ê–ù–ò–ö–ò: %v\n", r)
			fmt.Println("   –°—Ç–µ–∫—Ç—Ä–µ–π—Å –ø–∞–Ω–∏–∫–∏:")
			fmt.Println(getStackTrace())
			c.PanicCount++
			c.Willpower = max(c.Willpower-20, 0)
		}
	}()

	// –°–∏–º—É–ª—è—Ü–∏—è –ø–∞–Ω–∏–∫–∏
	if c.Willpower < 20 {
		panic("–∫—Ä–∏—Ç–∏—á–µ—Å–∫–∏–π —É—Ä–æ–≤–µ–Ω—å —Å–∏–ª—ã –≤–æ–ª–∏: –¥–µ–ø—Ä–µ—Å—Å–∏—è")
	}
}

