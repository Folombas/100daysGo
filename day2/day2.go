package main

// Программа: "Анти-прокрастинатор на Go"
// Суть: Трекер, который анализирует мой день и даёт мотивирующую статистику

import (
	"fmt"
	"time"
)

func main() {
	// Данные моего дня
	today := time.Now()
	mood := "Июльская Жёсткая Депрессия"
	productiveHours := 1.5
	distractions := []string{"соцсети", "диван", "еда", "мысли о пляже", "самокопание", "отсутствие друзей", "одинокая жизнь"}

	correctDate := today.Format("02.01.2006")

	// Анализ дня
	fmt.Printf("📅 День 3: %s\n", correctDate)
	fmt.Println("===========================")

	// Блок: Оценка слитого дня
	fmt.Printf("Настроение: %s\n", mood)
	fmt.Printf("Продуктивных часов: %.1f\n", productiveHours)

	// Блок: Анализ депрессии и прокрастинации
	fmt.Println("\n Мои виды депрессий и прокрастинации:")
	for i, distraction := range distractions {
		fmt.Printf("%d. %s\n", i+1, distraction)
	}

	// Блок: Мотивация
	fmt.Println("\n💪 АНТИ-ПРОКРАСТИНАЦИОННЫЕ ФАКТЫ:")
	fmt.Println("- Я написал код ВОПРЕКИ своему депрессивному состоянию.")
	fmt.Println("- 1.5 часа программирования за компьютером > 0 часов (лучше хоть что-то, чем вообще ничего)")
	fmt.Println("- Каждая строчка кода — кирпич в фундаменте новой жизни")

	// Прогресс марафона
	totalDays := 3 // 25,26,27 июля
	fmt.Printf("\n🏆 Прогресс марафона: %d/100 дней\n", totalDays)
	fmt.Println("──────────────────────────────────")
	fmt.Println("ЗАВТРАШНИЙ ПЛАН: Условные операторы!")
	fmt.Println("Напишем программу, которая предскажет мой первый IT-заработок")

}
