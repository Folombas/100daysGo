// generateDistractions —Å–æ–∑–¥–∞–µ—Ç –æ—Ç–≤–ª–µ—á–µ–Ω–∏—è –≤ —Ä–∞–∑–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏—è—Ö
func generateDistractions(category string, output chan<- Distraction, wg *sync.WaitGroup) {
	defer wg.Done()

	distractionTypes := []string{
		"–í–æ—Å–ø–æ–º–∏–Ω–∞–Ω–∏—è –æ –º–æ—Ä–µ",
		"–ñ–µ–ª–∞–Ω–∏–µ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å",
		"–ú—ã—Å–ª–∏ –æ –¥–µ–Ω—å–≥–∞—Ö",
		"–î–∞–≤–ª–µ–Ω–∏–µ —Å–µ–º—å–∏",
		"–£—Å—Ç–∞–ª–æ—Å—Ç—å –æ—Ç Go",
		"–ò–¥–µ–∏ –¥–ª—è –≤–∏–¥–µ–æ",
		"–ú–µ—á—Ç—ã –æ —Å–ª–∞–≤–µ",
		"–ñ–∞–ª–æ—Å—Ç—å –∫ —Å–µ–±–µ",
		"–°–æ–º–Ω–µ–Ω–∏—è –≤ —É—Å–ø–µ—Ö–µ",
		"–ñ–µ–ª–∞–Ω–∏–µ —Ä–∞–∑–≤–ª–µ—á–µ–Ω–∏–π",
	}

	for i := 0; i < 5; i++ { // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º 5 –æ—Ç–≤–ª–µ—á–µ–Ω–∏–π –Ω–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏—é
		time.Sleep(time.Duration(rand.Intn(500)) * time.Millisecond)

		distractionType := distractionTypes[rand.Intn(len(distractionTypes))]
		strength := distractionStrengths[distractionType]

		desc := fmt.Sprintf("[%s] %s", category, distractionType)

		distraction := Distraction{
			Type:        distractionType,
			Strength:    strength,
			Description: desc,
			Reward:      strength * 10, // –ù–∞–≥—Ä–∞–¥–∞ –ø—Ä–æ–ø–æ—Ä—Ü–∏–æ–Ω–∞–ª—å–Ω–∞ —Å–ª–æ–∂–Ω–æ—Å—Ç–∏
		}

		output <- distraction
	}
}

// fanIn –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ –∫–∞–Ω–∞–ª–æ–≤ –≤ –æ–¥–∏–Ω (–ø–∞—Ç—Ç–µ—Ä–Ω Fan-In)
func fanIn(sources []chan Distraction) <-chan Distraction {
	combined := make(chan Distraction, 20)
	var wg sync.WaitGroup

	// –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è –∏–∑ –∫–∞–∂–¥–æ–≥–æ –∏—Å—Ç–æ—á–Ω–∏–∫–∞ –≤ –æ–±—ä–µ–¥–∏–Ω–µ–Ω–Ω—ã–π –∫–∞–Ω–∞–ª
	output := func(source <-chan Distraction) {
		defer wg.Done()
		for distraction := range source {
			combined <- distraction
		}
	}

	wg.Add(len(sources))
	for _, source := range sources {
		go output(source)
	}

	// –ó–∞–∫—Ä—ã–≤–∞–µ–º –æ–±—ä–µ–¥–∏–Ω–µ–Ω–Ω—ã–π –∫–∞–Ω–∞–ª –ø–æ—Å–ª–µ –∑–∞–∫—Ä—ã—Ç–∏—è –≤—Å–µ—Ö –∏—Å—Ç–æ—á–Ω–∏–∫–æ–≤
	go func() {
		wg.Wait()
		close(combined)
	}()

	return combined
}

