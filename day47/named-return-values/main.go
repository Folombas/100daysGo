// getDayStats –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏–º–µ–Ω–æ–≤–∞–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è: —ç–º–æ—Ü–∏–∏, –¥–æ—Ö–æ–¥ –∏ —É—Ä–æ–≤–µ–Ω—å –Ω–∞–¥–µ–∂–¥—ã
func getDayStats(orders int, visitedLuxury bool, hasHomeDinner bool) (mood string, earnings float64, hopeLevel int) {
	// –ë–∞–∑–æ–≤—ã–µ –∏–º–µ–Ω–æ–≤–∞–Ω–Ω—ã–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
	mood = "–¥–µ–ø—Ä–µ—Å—Å–∏–≤–Ω—ã–π"
	earnings = float64(orders) * 350
	hopeLevel = 30

	// –ú–æ–¥–∏—Ñ–∏—Ü–∏—Ä—É–µ–º –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏—Ö —Å–æ–±—ã—Ç–∏–π
	if orders > 0 {
		mood = "—É—Å—Ç–∞–ª—ã–π, –Ω–æ –≤—ã–ø–æ–ª–Ω–∏–ª –æ–±—è–∑–∞—Ç–µ–ª—å—Å—Ç–≤–∞"
		hopeLevel += 20
	}

	if visitedLuxury {
		mood = "–∫–æ–Ω—Ç—Ä–∞—Å—Ç–Ω—ã–π: –≤–∏–¥–µ–ª —Ä–æ—Å–∫–æ—à—å, –Ω–æ –≤–µ—Ä–Ω—É–ª—Å—è –≤ —Ä–µ–∞–ª—å–Ω–æ—Å—Ç—å"
		hopeLevel -= 10 // –≤—Ä–µ–º–µ–Ω–Ω—ã–π —Å–ø–∞–¥ –∏–∑-–∑–∞ –∫–æ–Ω—Ç—Ä–∞—Å—Ç–∞
	}

	if hasHomeDinner {
		mood += " + –±–ª–∞–≥–æ–¥–∞—Ä–Ω—ã–π –∑–∞ –º–∞–º–∏–Ω —É–∂–∏–Ω"
		hopeLevel += 35 // –º–∞–º–∏–Ω —É–∂–∏–Ω = –º–æ—â–Ω—ã–π –∑–∞—Ä—è–¥ –Ω–∞–¥–µ–∂–¥—ã
		earnings -= 100 // —Å–∏–º–≤–æ–ª–∏—á–µ—Å–∫–∞—è "–ø–ª–∞—Ç–∞" –∑–∞ —ç–º–æ—Ü–∏–æ–Ω–∞–ª—å–Ω–æ–µ –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ
	}

	// –ò–º–µ–Ω–æ–≤–∞–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤–æ–∑–≤—Ä–∞—â–∞—é—Ç—Å—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏
	return
}

